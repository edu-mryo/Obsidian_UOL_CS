function setup()
{
	//create a large square canvas
	createCanvas(800, 800);
}

function draw()
{

	//set the fill colour to red
	fill(255, 0, 0);

	//set a thick stroke weight for the black lines
	strokeWeight(12);

	//draw the red rectangle 
	rect(100, 50, 600, 600);

	//set the fill colour to blue
	fill(0, 0, 255);
	rect(100, 50, 100, 200);

	//set the fill colour to yellow
	fill(255, 255, 0);
	rect(600, 50, 100, 400);

	//set the fill colour to white
	fill(255, 255, 255);
	rect(100, 250, 100, 400);

	//set the fill colour to black
	fill(0, 0, 0);
	rect(200, 50, 400, 100);

	//set the fill colour to pink
	fill(255, 0, 255);
	rect(200, 150, 200, 100);

	//set the fill colour to green
	fill(0, 255, 0);
	rect(400, 150, 200, 300);

	//set the fill colour to brown	
	fill(139, 69, 19);
	rect(200, 250, 200, 200);

	//set the fill colour to orange
	fill(255, 165, 0);
	rect(400, 450, 100, 200);

	//set the fill colour to white
	fill(255, 255, 255);
	rect(500, 450, 100, 200);

	//set the fill colour to black
	fill(0, 0, 0);
	rect(600, 450, 100, 200);

	//set the fill colour to blue
	fill(0, 0, 255);
	rect(200, 450, 200, 70);

	//set the fill colour to yellow
	fill(255, 255, 0);
	rect(200, 520, 200, 130);


}