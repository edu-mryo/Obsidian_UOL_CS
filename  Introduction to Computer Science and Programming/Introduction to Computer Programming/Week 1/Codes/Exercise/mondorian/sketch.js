function setup()
{
	//create a large square canvas
	createCanvas(800, 800);
}

function draw()
{

	//set the fill colour to red
	fill(255, 0, 0);

	//set a thick stroke weight for the black lines
	strokeWeight(10);

	//draw the red rectangle 
	rect(100, 50, 600, 600);
	

	rect(100,50,100,100);
	
	fill(0,0,255);
	rect(100,150,100,200);

	fill(255,255,255);
	rect(100,360,200,290);


	fill(255,255,0);
	rect(200,50,500,100);

	


	fill(0,255,0);
	rect(200,150,300,200);

	fill(0,255,255);
	rect(500,150,200,200);

	fill(255,0,255);
	rect(200,360,400,290);

	fill(255,255,255);
	rect(600,360,100,150);

	fill(100,100,100);
	rect(600,510,100,140);
	



}